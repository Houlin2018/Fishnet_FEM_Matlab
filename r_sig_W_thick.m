function [sig_0] = r_sig_W_thick(num_e)
%*************************************************************************
%
%    |===\         /====|   |=|     |===|       =      =      =    
%    |    |       |          |     /             \    / \    /  
%    |===/         \===\     |    |     ==|       \  /   \  /   
%    |   \              |    |     \     /         \/     \/     
%    =    =  ___  |====/    |=|     |===|   ____   ==     ==      _THICK
%                                                            
%*************************************************************************
%   RANDOM STRENGTH GENERATOR (WEIBULL m=5, x_0=10)
%                        
%       Input: num_e = total number of elements
%
%       Output: sig_0 = vector of random strength
%
%
%   Wen Luo
%   5/16/2017
%*************************************************************************
%*************************************************************************  
    rng('shuffle');
    p0 = rand(num_e,1);
    sig_0 = zeros(num_e,1);
    for ii = 1:num_e
       sig_0(ii) = 10*(-log(1-p0(ii)))^(1/5);
    end
end