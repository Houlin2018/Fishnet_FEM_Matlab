function [sig_0] = r_sig_W(num_e)
%*************************************************************************
%
%       |===\         /====|   |=|     |===|       =      =      =    
%       |    |       |          |     /             \    / \    /  
%       |===/         \===\     |    |     ==|       \  /   \  /   
%       |   \              |    |     \     /         \/     \/       
%       =    =  ___  |====/    |=|     |===|   ____   ==     ==    
%  
%*************************************************************************
%   RANDOM STRENGTH GENERATOR (WEIBULL)
%                        
%       Input: num_e = total number of elements
%
%       Output: sig_0 = vector of random strength
%
%
%   Wen Luo
%   2/6/2017
%*************************************************************************
%*************************************************************************  
rng('shuffle');
p0 = rand(num_e,1);
sig_0 = zeros(num_e,1);
a = 10;
b = 13.5;
for ii = 1:num_e
   sig_0(ii) = a * (-log(1-p0(ii)))^(1/b);
end
end