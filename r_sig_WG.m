function [sig_0] = r_sig_WG(num_e)
%*************************************************************************
%
%    |===\         /====|   |=|     |===|       =      =      =    |===|  
%    |    |       |          |     /             \    / \    /    /       
%    |===/         \===\     |    |     ==|       \  /   \  /    |     ==|
%    |   \              |    |     \     /         \/     \/      \     / 
%    =    =  ___  |====/    |=|     |===|   ____   ==     ==       |===|
%  
%*************************************************************************
%   RANDOM STRENGTH GENERATOR (WEIBULL - GAUSSIAN)
%                        
%       Input: num_e = total number of elements
%
%       Output: sig_0 = vector of random strength
%
%
%   Wen Luo
%   2/6/2017
%*************************************************************************
%*************************************************************************  
rng('shuffle');
p0 = rand(num_e,1);
sig_0 = zeros(num_e,1);
for ii = 1:num_e
   if p0(ii) <= 0.102792
       sig_0(ii) = 12*(-log(1-0.392074 * p0(ii)))^(1/10);
   elseif p0(ii) > 0.102792
       sig_0(ii) = 10 + 1.131371 * erfinv(2.10872 * p0(ii) - 1.108717);
   else
       sig_0(ii) = 8.6;
   end
end
end
